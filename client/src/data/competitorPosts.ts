export const getAllCompetitorPosts = (): BlogPost[] => {
  return [...competitorPosts, ...eposPosts];
};

export const getCompetitorPostBySlug = (slug: string): BlogPost | undefined => {
  return getAllCompetitorPosts().find(post => post.slug === slug);
};
